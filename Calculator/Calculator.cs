internal class Calculator
{
    public double Value;

    public double Sum(double value)
    {
        return Value = Value + value;
    }
    public double Subtract(double value)
    {
        return Value = Value - value;
    }

    public double Divide(double value)
    {
        return Value = Value / value;
    }

    public double Multiply(double value)
    {
        return Value = Value * value;
    }

}